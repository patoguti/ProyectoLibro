    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        BufferedWriter mbrWriter = null;
        BufferedWriter mbrWriter2 = null;
        try {
            Gson gson = new GsonBuilder().setPrettyPrinting().create();
            String archivo = gson.toJson(this.cursos);
            String archivoApoderado = gson.toJson(this.apoderados);
            mbrWriter = new BufferedWriter(new FileWriter("cursosTotal.json"));
            mbrWriter.write(archivo);
            mbrWriter.close();

            mbrWriter2 = new BufferedWriter(new FileWriter("apoderados.json"));
            mbrWriter2.write(archivoApoderado);
            mbrWriter2.close();
        } catch (IOException ex) {
            Logger.getLogger(VentanaInicio.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                mbrWriter.close();
                mbrWriter2.close();
            } catch (IOException ex) {
                Logger.getLogger(VentanaInicio.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

