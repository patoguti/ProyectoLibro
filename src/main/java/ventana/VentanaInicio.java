    private void cargarCurso() {
        this.cursoSeleccionado = 0;
        this.asignaturaSeleccionada = 0;

        Gson gson = new GsonBuilder().create();
        BufferedReader bufRead = null;
        try {
            bufRead = new BufferedReader(new FileReader("cursosTotal.json"));
        } catch (FileNotFoundException ex) {
            Logger.getLogger(VentanaInicio.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.cursos = gson.fromJson(bufRead, Curso[].class);
    }

    private void cargarAlumnado() {
        String[] run = new String[30];
        for (int i = 0; i < 30; i++) {
            run[i] = this.cursos[this.cursoSeleccionado].getAlumnos()[i].getRun();

        }
        jTextField1.setText(this.cursos[this.cursoSeleccionado].getAlumnos()[0].getNombre() + " "
                + this.cursos[this.cursoSeleccionado].getAlumnos()[0].getApellido());
        jComboBox4.setModel(new javax.swing.DefaultComboBoxModel<>(run));
        this.buscarApoderado();
    }

    private void cargarApoderado() {
        Gson gson = new GsonBuilder().create();
        BufferedReader bufRead = null;
        try {
            bufRead = new BufferedReader(new FileReader("apoderados.json"));
        } catch (FileNotFoundException ex) {
            Logger.getLogger(VentanaInicio.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.apoderados = gson.fromJson(bufRead, Apoderado[].class);

        for (int i = 0; i < this.apoderados.length; i++) {
            for (int pupilos = 0; pupilos < this.apoderados[i].getPupilos().size(); pupilos++) {
                for (int curso = 0; curso < 16; curso++) {
                    String rutAlum = this.apoderados[i].getPupilos().get(pupilos).getRun();
                    if (this.cursos[curso].estaAlumno(rutAlum)) {
                        this.apoderados[i].getPupilos().set(pupilos, this.cursos[curso].getAlumnoPorRut(rutAlum));
                    }
                }
            }
        }

    }

    private void buscarApoderado() {
        for (int i = 0; i < this.apoderados.length; i++) {
            for (int j = 0; j < this.apoderados[i].getPupilos().size(); j++) {
                if (this.apoderados[i].getPupilos().get(j).getRun().equals(jComboBox4.getSelectedItem())) {
                    jTextField2.setText(this.apoderados[i].getNombre() + " " + this.apoderados[i].getApellido());
                    break;
                }
            }

        }
    }

